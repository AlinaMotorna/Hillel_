namespace Working_with_class_constructors
{
    internal class Rectangle
    {
        int side1;
        int side2;

        public int Side1 {get;}
        public int Side2 { get; }
        public Rectangle(double side1, double side2)
        {
            this.side1 = Side1;
            this.side2 = Side2;
        }
       
    }
}
